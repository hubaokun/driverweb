package com.daoshun.guangda.NetData;

public class StudentCouponDetail {
     private String phone;
     
     private String name;
     
     private Integer couponusenumber;
     
     private Integer couponpaynumber;

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getCouponusenumber() {
		return couponusenumber;
	}

	public void setCouponusenumber(Integer couponusenumber) {
		this.couponusenumber = couponusenumber;
	}

	public Integer getCouponpaynumber() {
		return couponpaynumber;
	}

	public void setCouponpaynumber(Integer couponpaynumber) {
		this.couponpaynumber = couponpaynumber;
	}
     
     
}
